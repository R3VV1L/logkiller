#!/usr/bin/env bash

echo "Место в каталогах до удаления логов:"
df -h
echo -e "\n"
echo "///////////////////////////////////////////////////////////////"
sudo find /var/log -type f -name "*.log" -mtime +7 -exec rm -f {} \;

echo "Место после удаления логов:"
df -h

echo "///////////////////////////////////////////////////////////////"
echo -e "\n"
echo "Место в директориях:"
du -ah / --max-depth=1 | sort -hr
